def alphabetize(arr)
  # code here
  alphabet = {"a" => "00", "b" => "01", "c" => "02", "ĉ" => "03", "d" => "04", "e" => "05", "f" => "06", "g" => "07", "ĝ" => "08", "h" => "09",
              "ĥ" => "10", "i" => "11", "j" => "12", "ĵ" => "13", "k" => "14", "l" => "15", "m" => "16", "n" => "17", "o" => "18", "p" => "19",
			  "r" => "20", "s" => "21", "ŝ" => "22", "t" => "23", "u" => "24", "ŭ" => "25", "v" => "26", "z" => "27"}
  arr.sort_by { |word| word.split(//).map{ |letter| alphabet[letter] }.join }
end